<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use App\Supplier;

class SupplierController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $suppliers = Supplier::all();
        return view('suppliers.index',compact('suppliers'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('suppliers.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'supplier_id'=>'required',
            'name'=>'required',
        ]);

        $supplier = new Supplier([
            'supplier_id'=>$request->get('supplier_id'),
            'name'=>$request->get('name'),
        ]);

        $supplier->save();
        return redirect('/suppliers')->with('success', 'Supplier saved!');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $supplier = Headquarters::where('supplier_id',$id)->first();
        return view('suppliers.edit', compact('supplier'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'supplier_id'=>'required',
            'name'=>'required',
        ]);


        $supplier = Supplier::where('supplier_id',$id)->first();
        $supplier->supplier_id = $request->get('supplier_id');
        $supplier->name = $request->get('name');
        $supplier->save();

        return redirect('/suppliers')->with('success','Supplier updated!')
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $supplier = Supplier::where('supplier_id',$id)->first();
        $supplier->delete();
        return redirect('/suppliers')->with('success','Supplier deleted!');
    }
}
